package aula2;

public class LoopWhileAndforRevers {
	
	public static void main (String[]args) {
		
		int i = 20;
		
		while(i >=0) {
			System.out.print(i);
			i--;
		}
		
		String espaco = " \n ";
		System.out.println(espaco);

		
		for(int j = 20; j >=0 ; j--) {			
			System.out.print(j);
			
		}
	}

}
